package uppgift5;

public class Account {
	private String nbr;
	private double balance;

	
	
	public String getNbr() {
		return this.nbr;
	}
	public void setNbr(String nbr) {
		this.nbr = nbr;
	}
	public double getBalance() {
		return this.balance;
	}
	public void setBalance(double balance) {
		this.balance = balance;
	}

}
